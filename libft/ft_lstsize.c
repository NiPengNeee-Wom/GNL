#include "libft.h"

int		ft_lstsize(t_lst **alst)
{
	int		i;
	t_lst	*tmp;

	i = 0;
	tmp = *alst;
	while (tmp != NULL)
	{
		tmp = tmp->next;
		i++;
	}
	return (i);
}
