#include "libft.h"

char	*ft_strnew(size_t size)
{
	char *s;

	s = malloc(sizeof(char) * (size) + 1);
	if (!s)
		return (NULL);
	while (size)
		s[size--] = '\0';
	*s = '\0';
	return (s);
}
