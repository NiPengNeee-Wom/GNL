#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		i;
	char	*dest;
	int		l;

	l = ft_strlen(s1);
	i = 0;
	dest = (char *)malloc(sizeof(char) * l + 1);
	if (dest == NULL)
		return (0);
	while (s1[i] != '\0')
	{
		dest[i] = s1[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
